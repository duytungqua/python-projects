a = 5
b = 10

# Arithmetic Operations
sum_result = a + b
diff_result = b - a
prod_result = a * b
div_result = b / a
mod_result = b % a
exp_result = a ** 2

print("Sum:", sum_result)
print("Difference:", diff_result)
print("Product:", prod_result)
print("Division:", div_result)
print("Modulus:", mod_result)
print("Exponentiation:", exp_result)

# Assignment Operations
a += 2  
b *= 3  
print("Updated a:", a)
print("Updated b:", b)          

# Comparison Operations
print("a == b:", a == b)
print("a != b:", a != b)
print("a < b:", a < b)
print("a <= b:", a <= b)
print("a > b:", a > b)
print("a >= b:", a >= b)

# Logical Operations
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)      

